package org.janusproject.demos.network.januschat.agent;

import java.util.EventListener;

import org.janusproject.kernel.address.AgentAddress;

/**
 * The listener used to propagate incoming private messages.
 * 
 * @author $Author: ngaud$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 */
public interface IncomingPrivateMessageListener extends EventListener {

	/**
	 * Invoked when a privatemessage is arrived.
	 * 
	 * @param sender is the message sender
	 * @param message is the message.
	 */
	public void incomingPrivateMessage(AgentAddress sender, String message);
	
	
}
