package org.janusproject.jaak.turtle;

import org.janusproject.jaak.envinterface.body.TurtleBody;
import org.janusproject.jaak.envinterface.body.TurtleBodyFactory;
import org.janusproject.kernel.channels.Channel;

/** This class permits to define a Turtle in Jaak.
 * 
 * @author $Author: sgalland$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 */
public interface TurtleBindingChannel extends Channel {

	/** Request the owner of this channel to be binded to a situated body
	 * in a Jaak environment.
	 * <p>
	 * If the turtle is already binded, this action has not effect.
	 * 
	 * @param bodyFactory is the body factory to use.
	 * @return the created body or <code>null</code> if none.
	 */
	public TurtleBody bindBody(TurtleBodyFactory bodyFactory);

}