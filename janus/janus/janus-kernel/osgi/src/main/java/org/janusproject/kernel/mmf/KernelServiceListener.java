import java.util.EventListener;


/**
 * Listener on events for OSGi module of the Janus kernel.
 * 
 * @author $Author: srodriguez$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 */
public interface KernelServiceListener extends EventListener {

	/**
	 * Invoked when an event occurs inside the OSGi implementation
	 * of the Janus kernel.
	 * 
	 * @param event describes the event.
	 */
	public void kernelServiceEvent(KernelServiceEvent event);
	
}
