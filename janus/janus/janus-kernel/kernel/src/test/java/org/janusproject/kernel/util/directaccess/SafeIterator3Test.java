package org.janusproject.kernel.util.directaccess;

import java.util.logging.Level;

import org.janusproject.kernel.logger.LoggerUtil;
import junit.framework.TestCase;

/**
 * @author $Author: sgalland$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 */
public class SafeIterator3Test extends TestCase {

	private SafeIterator<Integer> iterator;
	
	/**
	 * @throws Exception
	 */
	@Override
	public void setUp() throws Exception {
		super.setUp();
		LoggerUtil.setGlobalLevel(Level.OFF);
		this.iterator = new SafeIterator<Integer>();
	}
	
	/**
	 * @throws Exception
	 */
	@Override
	public void tearDown() throws Exception {
		this.iterator = null;
		super.tearDown();
	}

	/**
	 */
	public void testIterator() {
		assertFalse(this.iterator.hasNext());
	}

}
