package org.janusproject.kernel.crio.capacity;

import java.util.Collection;

/**
 * This interface permits to apply a selection policy to a
 * collection of capacity implementations.
 * 
 * @author $Author: sgalland$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 */
public interface CapacityImplementationSelectionPolicy {
	
	/** Applies a selection policy to get one implementation of the
	 * given capacity.
	 * 
	 * @param capacities is the collection of available capacities. 
	 * @return the capacity implementation or <code>null</code> if none inside.
	 */
	public CapacityImplementation selectImplementation(Collection<? extends CapacityImplementation> capacities);

}
