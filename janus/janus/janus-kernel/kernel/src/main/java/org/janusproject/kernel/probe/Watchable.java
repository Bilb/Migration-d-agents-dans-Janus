package org.janusproject.kernel.probe;

import static java.lang.annotation.RetentionPolicy.RUNTIME;

import java.lang.annotation.ElementType;
import java.lang.annotation.Retention;
import java.lang.annotation.Target;

/** Annotation that permits to mark classes and fields as watchable.
 * 
 * @author $Author: sgalland$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 */
@Retention(value=RUNTIME)
@Target({ElementType.TYPE,ElementType.FIELD})
public @interface Watchable {

	/** List of names of the inherit field to watch.
	 */
	String[] inheritFields() default {};

	/** Indicates if also watchable from subclasses.
	 */
	boolean isFinal() default false;
}