package org.janusproject.kernel.crio.role;

import org.janusproject.kernel.crio.core.Role;

/**
 * This interface is used by <code>Group</code>
 * to create an instance of a role.
 * 
 * @author $Author: sgalland$
 * @version $FullVersion$
 * @mavengroupid $GroupId$
 * @mavenartifactid $ArtifactId$
 */
public interface RoleFactory {

	/** Invoked to obtain a new instance of the role.
	 * 
	 * @param type is the role type.
	 * @return the new role instance.
	 * @throws Exception if something wrong append
	 */
	public Role newInstance(Class<? extends Role> type) throws Exception;

}
